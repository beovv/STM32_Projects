void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static GPIO_PinState A,B,prev_A=GPIO_PIN_SET,prev_B=GPIO_PIN_SET;

	if(GPIO_Pin==RENC_A_Pin)
	{
		B=HAL_GPIO_ReadPin(RENC_B_GPIO_Port,RENC_B_Pin);
		// CW decoder
		if(B!=prev_B)
		{
			if(B==GPIO_PIN_SET)
			{
				if(A==GPIO_PIN_RESET)
				{
					count=count+6;
					send_usart=1;
					//HAL_Delay(10);
				}
			}
			prev_B=B;
		}

	}

	if(GPIO_Pin==RENC_B_Pin)
	{
		A=HAL_GPIO_ReadPin(RENC_A_GPIO_Port,RENC_A_Pin);
		// CCW decoder
		if(A!=prev_A)
		{
			if(A==GPIO_PIN_SET)
			{
				if(B==GPIO_PIN_RESET)
				{
					count=count-6;
					send_usart=1;
					//HAL_Delay(10);
				}
			}
			prev_A=A;
		}

	}
}
